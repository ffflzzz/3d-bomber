/** 获取英雄的随机台词 */
export function getHeroDialogue(heroId: string, category: keyof HeroDialogueSet, abilityKey?: string): string {
  const dialogues = HERO_DIALOGUES[heroId]
  if (!dialogues) return ''
  
  if (category === 'ability' && abilityKey) {
    const abilityLines = (dialogues.ability as Record<string, string[]>)[abilityKey]
    if (!abilityLines || abilityLines.length === 0) return ''
    return abilityLines[Math.floor(Math.random() * abilityLines.length)]
  }
  
  const lines = dialogues[category]
  if (!Array.isArray(lines) || lines.length === 0) return ''
  return lines[Math.floor(Math.random() * lines.length)]
}